export function mc2 (m: any){
    if(m == 6){
        return 6 * Math.pow(299792458,2);
    }
    if(m == 5){
        return 5 * Math.pow(299792458,2);
    }
    if(m == 7){
        return 7 * Math.pow(299792458,2);
    }
    if(m == 10){
        return 10 * Math.pow(299792458,2);
    }
    return 0;
}